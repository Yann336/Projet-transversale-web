    <h1>FAQ-Questions les plus fréquentes</h1>
    <p><strong>Comment procéder à un retour de marchandise ? </strong>

Vous souhaitez nous retourner un ou plusieurs articles ? Pas de problème, cliquez simplement sur le lien "Portail de retour".

Vous avez déjà enregistré votre retour sur le portail ? Patience, notre service client vous enverra très prochainement le bon de retour à coller sur votre colis.

Attendez bien de recevoir l'étiquette de retour prépayée, nous ne serons pas en mesure de vous rembourser les frais engendrés si vous choisissez de retourner le produit par vos propres moyens.

Merci de nous renvoyer le ou les articles complets, si possible dans l’emballage d’origine. Si vous ne l’avez pas conservé, veuillez opter pour une alternative adéquate. Pour nous permettre d’améliorer nos prestations à l’égard de la clientèle, nous vous serions reconnaissants de bien vouloir préciser la raison du retour.</p>

<p><strong>Quelle est la durée de la garantie pour un produit ?</strong>

Tous les produits présentés sur notre site sont neufs et disposent par conséquent, d’une garantie légale de 2 ans couvrant les vices cachés et les risques de non conformité. La garantie entre en vigueur à réception de la marchandise.</p>

<p><strong>Les délais de livraison des articles de ma commande sont differents. Quand serai-je livré?</strong>
La totalité de votre commande sera livrée sur la base du délai le plus long dans la majorité des cas. Si une livraison partielle de votre commande est nécessaire, nous vous invitons à nous contacter afin d’en définir les conditions.</p>

<p><strong>Qui prend en charge les frais de retour ?</strong>
 
Les frais de retour sont à notre charge, vous ne payez rien !</p>
